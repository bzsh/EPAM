package by.etc.class_task.class_object.task_two;

public class Test2 {
    private int x;
    private int y;

    public Test2(){
        x = 0;
        y = 0;
    }

    public Test2(int x, int y){
        this.x = x;
        this.y = y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
